        [Fact]
        public void CalculateChecksum_ArgumentExceptions()
        {
            var b = new byte[] { 0x00 };

            Should.NotThrow(() => Flasher.CalculateChecksum(b, 0, 1));
            Should.Throw<ArgumentOutOfRangeException>(() => Flasher.CalculateChecksum(b, -1, 1));
            Should.Throw<ArgumentOutOfRangeException>(() => Flasher.CalculateChecksum(b, 0, 0));
            Should.Throw<ArgumentOutOfRangeException>(() => Flasher.CalculateChecksum(b, 0, -1));
            Should.Throw<ArgumentNullException>(() => Flasher.CalculateChecksum(null!, 0, 1));
            Should.Throw<ArgumentException>(() => Flasher.CalculateChecksum(b, 0, 2));
            Should.Throw<ArgumentException>(() => Flasher.CalculateChecksum(b, 1, 1));
        }

