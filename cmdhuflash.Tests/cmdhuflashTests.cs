        public void CreateDataPacket(int offset)
        {
            var b = new byte[1_048_576];
            Random.Shared.NextBytes(b);
            var packet = Flasher.CreateDataPacket(b, offset, false);
            packet.Length.ShouldBe(261);
            packet[0].ShouldBe<byte>(0x5A);

            // These are covered by another test and must be correct
            var expectedAddress = Flasher.AddressToBytes(offset);
            var bytesForChecksum = new byte[256 + 3];
            bytesForChecksum[0] = expectedAddress.msb;
            bytesForChecksum[1] = expectedAddress.middle;
            bytesForChecksum[2] = expectedAddress.lsb;
            Array.Copy(b, offset, bytesForChecksum, 3, 256);
            var expectedChecksum = Flasher.CalculateChecksum(bytesForChecksum, 0, bytesForChecksum.Length);

            packet[1].ShouldBe(expectedAddress.msb);
            packet[2].ShouldBe(expectedAddress.middle);
            packet[3].ShouldBe(expectedAddress.lsb);
            packet[4..260].ShouldBe(b[offset..(offset + 256)]);
            packet[260].ShouldBe(expectedChecksum);
        }
